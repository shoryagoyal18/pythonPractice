# Declaring list
list1 = [1, 2, 3, "Shorya", False] 

# Printing list
print(list1)

# Printing length of list 
print(len(list1))

# Accessing list element in python 
print(list1[2])
print(list1[-1]) # This will print the last element of the list 

# Changing element of list 
list1[2] = 5
print(list1)

# Iterating on list 
for i in list1:
    print(i)
    pass

# Iterating on list with while loop 
while i < len(list1):
    print(list1[i])
    i += 1

