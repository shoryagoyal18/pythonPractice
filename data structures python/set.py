# Properties of set 
# 1. Duplicates are not allowed 
# 2. Items are unordered  
# 3. Set can contain mixed data type

set1 = {1, 2, 3, 3} 

# Printing the set 
print(set1)

# Printing length of set 
print(len(set1))

# Adding an item in a set 
set1.add(4)

# Removing element in a set 
set1.discard(2)

# iterating on set 
for i in set1:
    print(i)

